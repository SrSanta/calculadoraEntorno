public class calculadora {
    public int op1;
    public int op2;
    public int resultado;
    
    int suma(int op1, int op2){
        return this.resultado = op1 + op2;
    }
    
    int resta(int op1, int op2){
        return this.resultado = op1 - op2;
    }
    
    int multiplicar(int op1, int op2){
        return this.resultado = op1 * op2;
    }
    
    int dividir(int op1, int op2){
        return this.resultado = op1 / op2;
        
    }
    
    
    
}
